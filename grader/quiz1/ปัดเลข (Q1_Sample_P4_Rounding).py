
n,p = [ int(e) for e in input().split() ]
i = (n // 10**(p-1)) %10 # digit in the rounding position 1231[2]3 2
if i<5:
    n = (n // 10**p) * 10**p #123123//100 = 1231 -> * 100
elif i>5:#12[7]89 3
    n = (n // 10**p) * 10**p + 10**p #12789//1000 = 12 ->  *1000 -> 12000 + 1000 -> 13000
elif i == 5: #12[5] 1, 13[5]5 2
    if ((n // 10**(p)) %10) % 2 == 0:
        n = (n // 10**p) * 10**p#125//10 = 12 *10 -> 120
    else:
        n = (n // 10**p) * 10**p + 10**p#1355//100 = 13 *100 ->1300+ 100= 1400
print(n)


