#แบ่งเป็น 100000 400000 500000 1e6 3e6  ต้องใช้ช่วงนี้เทียบ(ราคาสะสม)
n = float(input())
t = 0
if 1<=n<=1e5:
  t += n*0.05
elif 1e5+1<=n<=5e5:
  t += (n-1e5)*0.10 + 5000 #100000*0.5
elif 5e5+1<=n<=1e6:
  t += (n-5e5)*0.20 +45000 #100000*0.5 + 400000*0.1
elif 1e6+1<=n<=4e6:
  t += (n-1e6)*0.30 +145000 #100000*0.5 + 400000*0.1 + 500000*0.2
elif n>=4e6+1:
  t += (n-4e6)*0.37 +1045000 #100000*0.5 + 400000*0.1 + 500000*0.2 +1e6*0.3
print(float(t))
