#versionกากสัสๆ คิดเยอะ
if 1<=income<1e5:
    tax += income*5/100    
elif income>=1e5:
    tax += 1e5*5/100
    income -= 1e5
    if income<4e5: #income= income_total-1e5 < 4e5 income_total<5e5...
        tax += income*10/100
    elif income>=4e5:
        tax += 4e5 *10/100 #รวมกับแสนแรกละจะได้ 1e5+4e5$$$ =5e5 ไม่ใช่เริ่มจากใหม่
        income -= 4e5    #$$$
        if income<5e5:
            tax += income*20/100
        elif income>=5e5:
            tax += 5e5 *20/100 #1e5+4e5 +5e5$$$ =1e6
            income -= 5e5 #$$$
            if income<3e6:
                tax += income*30/100
            elif income>= 3e6:
                tax+= 3e6 *30/100 #1e5+4e5 +5e5 +(3e6)$$$ =4e6
                income -= 3e6 #$$$
                tax += income*37/100 #ไม่เหลือไรจะลบละ
print(tax)
